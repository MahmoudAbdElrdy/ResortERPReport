app.directive('departments', function ($rootScope, $compile) {
    return {
        restrict: 'E',
        scope: false,
        templateUrl: function (scope, element) {
            var tempurl = '../../NgApp/Departments/Views/' + $rootScope.language + '/departments.html';
            $rootScope.$watch(function () { return '../../NgApp/Departments/Views/' + $rootScope.language + '/departments.html'; }
                , function (newVal, oldVal) {
                    if (newVal && newVal !== oldVal) {
                        element.$$element.html(newVal);
                        $compile(element.$$element)(scope);
                    }
                });
            return tempurl;
        }
    }
})